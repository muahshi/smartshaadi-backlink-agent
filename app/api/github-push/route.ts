export async function POST(req: NextRequest) {
  try {
    const { leads, runDate } = await req.json() as {
      leads: BacklinkLead[];
      runDate: string;
    };

    if (!leads || leads.length === 0) {
      return Response.json({ error: 'No leads provided' }, { status: 400 });
    }

    // Generate markdown
    const markdown = generateMarkdown(leads, runDate);

    // Push to GitHub
    const commitMsg = `🔗 Backlink Agent: ${leads.length} leads found on ${runDate.split('T')[0]}`;
    const result = await pushToGitHub(markdown, commitMsg);

    return Response.json({
      success: true,
      leadsCount: leads.length,
      highValue: leads.filter(l => l.relevanceScore >= 70).length,
      githubUrl: result.url,
      sha: result.sha,
      message: `✅ ${leads.length} leads GitHub pe push ho gaye!`,
    });

  } catch (err) {
    console.error('GitHub push error:', err);
    return Response.json(
      { success: false, error: (err as Error).message },
      { status: 500 }
    );
  }
}

